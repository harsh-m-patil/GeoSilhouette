func main() {
	country := countryMaps()
	data, err := os.ReadFile("./ascii-countries/40-wide/" + country + ".txt")
	if err != nil {
		fmt.Printf("Error reading file %v", err)
	}
	fmt.Print(string(data))
	var guess string
	fmt.Printf("\nGuess : ")
	fmt.Scanf("%s", &guess)

	if guess == country {
		fmt.Println("You won")
	} else {
		fmt.Println("You lost")
		fmt.Println("It was " + country)
	}
}

